int	ft_atoi(const char *s)
{
	int	minus;
	int	total;
	int	i;

	minus = 1;
	total = 0;
	i = 0;
	while ((s[i] >= 9 && s[i] <= 13) || s[i] == 32)
		i++;
	if (s[i] == '-')
	{
		minus = -1;
		i++;
	}
	else if (s[i] == '+')
		s++;
	while (s[i] >= 48 && s[i] <= 57)
	{
		total = (total * 10) + (s[i] - 48);
		i++;
	}
	return (total * minus);
}
