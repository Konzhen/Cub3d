#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	srcsize;

	i = 0;
	if (*src == '\0')
		return (0);
	srcsize = ft_strlen(src);
	if (size == 0)
		return (srcsize);
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (srcsize);
}
